package com.davidlin54.fitbitchallenge;

public class RGBItem {
    private int r, g, b, dr, dg, db;

    public RGBItem(int r, int g, int b, int dr, int dg, int db) {
        this.r = r;
        this.g = g;
        this.b = b;
        this.dr = dr;
        this.dg = dg;
        this.db = db;
    }

    public int getR() {
        return r;
    }

    public int getG() {
        return g;
    }

    public int getB() {
        return b;
    }

    public int getDr() {
        return dr;
    }

    public int getDg() {
        return dg;
    }

    public int getDb() {
        return db;
    }
}
